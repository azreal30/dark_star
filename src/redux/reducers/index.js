function rootReducer(state = initialState, action) {
  if (action.type === actions.UPDATE_CHARACTER) {
    return { ...state, character: action.payload };
  }
  if (action.type === actions.SET_GAME_STARTED) {
    return { ...state, gameStarted: action.payload };
  }
  if (action.type === actions.SET_BATTLE_MODE) {
    return { ...state, battleMode: action.payload };
  }
  if (action.type === actions.SET_BOSS) {
    return { ...state, boss: action.payload };
  }
  if (action.type === actions.SET_CHAR_MENU) {
    return { ...state, charMenuScreen: action.payload };
  }
  if (action.type === actions.SET_MAP) {
    return { ...state, map: action.payload };
  }

  return state;
}

export default rootReducer;
